// Command gen produces tables_gen.go from the ISO 5426 .ucm mapping (the ICU
// Unicode character map for ISO 5426, the UNIMARC extended-Latin character set).
//
//	go run ./internal/iso5426/gen            # fetches the published .ucm
//	go run ./internal/iso5426/gen file.ucm   # uses a local copy
//
// Single-byte entries become the graphic table; two-byte entries (a combining
// mark byte followed by a base byte) become the precomposition table. The
// standalone combining marks are hand-maintained in iso5426.go.
package main

import (
	"bufio"
	"fmt"
	"net/http"
	"os"
	"regexp"
	"sort"
	"strconv"
	"strings"
)

const sourceURL = "https://raw.githubusercontent.com/eiro/p5-encode-iso5426/master/iso-5426.ucm"

// a CHARMAP line: <UXXXX> \xNN[\xMM] |0   (optional trailing comment)
var lineRe = regexp.MustCompile(`^<U([0-9A-Fa-f]+)>\s+((?:\\x[0-9A-Fa-f]{2})+)\s*\|0`)

func main() {
	src, err := source()
	if err != nil {
		fail(err)
	}

	type single struct {
		b byte
		r uint32
	}
	type pair struct {
		b0, b1 byte
		r      uint32
	}
	var singles []single
	var pairs []pair
	// For decode, the first mapping of a byte (or byte pair) wins; later .ucm
	// entries are Unicode-side alternates / Latin-1 identity fallbacks.
	seenByte := map[byte]bool{}
	seenPair := map[[2]byte]bool{}

	sc := bufio.NewScanner(strings.NewReader(src))
	in := false
	for sc.Scan() {
		line := strings.TrimSpace(sc.Text())
		switch {
		case line == "CHARMAP":
			in = true
			continue
		case line == "END CHARMAP":
			in = false
			continue
		}
		if !in {
			continue
		}
		m := lineRe.FindStringSubmatch(line)
		if m == nil {
			continue
		}
		r, _ := strconv.ParseUint(m[1], 16, 32)
		bytes := parseBytes(m[2])
		switch len(bytes) {
		case 1:
			if bytes[0] >= 0x80 && !seenByte[bytes[0]] { // ASCII is handled directly by the decoder
				seenByte[bytes[0]] = true
				singles = append(singles, single{bytes[0], uint32(r)})
			}
		case 2:
			k := [2]byte{bytes[0], bytes[1]}
			if !seenPair[k] {
				seenPair[k] = true
				pairs = append(pairs, pair{bytes[0], bytes[1], uint32(r)})
			}
		}
	}
	sort.Slice(singles, func(i, j int) bool { return singles[i].b < singles[j].b })
	sort.Slice(pairs, func(i, j int) bool {
		if pairs[i].b0 != pairs[j].b0 {
			return pairs[i].b0 < pairs[j].b0
		}
		return pairs[i].b1 < pairs[j].b1
	})

	var out strings.Builder
	out.WriteString("// Code generated by internal/iso5426/gen; DO NOT EDIT.\n")
	out.WriteString("// Source: " + sourceURL + " (ISO 5426 ICU character map).\n\n")
	out.WriteString("package iso5426\n\n")

	// The single-byte graphics are hand-maintained in iso5426.go (the standard
	// ISO 5426 letter/symbol blocks, per marc4j); this .ucm variant diverges there.
	_ = singles

	fmt.Fprintf(&out, "// iso5426Compose maps a combining-mark byte and a base byte to the precomposed\n// rune ISO 5426 stores them as (%d entries).\n", len(pairs))
	out.WriteString("var iso5426Compose = map[[2]byte]rune{\n")
	for _, p := range pairs {
		fmt.Fprintf(&out, "\t{0x%02X, 0x%02X}: 0x%04X,\n", p.b0, p.b1, p.r)
	}
	out.WriteString("}\n")

	if err := os.WriteFile("tables_gen.go", []byte(out.String()), 0o644); err != nil {
		fail(err)
	}
	fmt.Printf("wrote tables_gen.go: %d graphics, %d precompositions\n", len(singles), len(pairs))
}

func parseBytes(s string) []byte {
	var b []byte
	for _, part := range strings.Split(s, `\x`) {
		if part == "" {
			continue
		}
		v, _ := strconv.ParseUint(part, 16, 8)
		b = append(b, byte(v))
	}
	return b
}

func source() (string, error) {
	if len(os.Args) > 1 {
		b, err := os.ReadFile(os.Args[1])
		return string(b), err
	}
	resp, err := http.Get(sourceURL)
	if err != nil {
		return "", err
	}
	defer resp.Body.Close()
	if resp.StatusCode != http.StatusOK {
		return "", fmt.Errorf("GET %s: %s", sourceURL, resp.Status)
	}
	var sb strings.Builder
	buf := make([]byte, 32*1024)
	for {
		n, err := resp.Body.Read(buf)
		sb.Write(buf[:n])
		if err != nil {
			break
		}
	}
	return sb.String(), nil
}

func fail(err error) {
	fmt.Fprintln(os.Stderr, "gen:", err)
	os.Exit(1)
}
