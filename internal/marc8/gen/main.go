// Command gen produces tables_gen.go from the Library of Congress MARC-8 code
// tables (codetables.xml), the authoritative mapping between MARC-8 and Unicode.
//
// The LoC file is a US Government work in the public domain. Run with:
//
//	go run ./internal/marc8/gen            # fetches the published XML
//	go run ./internal/marc8/gen path.xml   # uses a local copy
//
// It emits one decode map per single-byte non-Latin set and a packed, base64
// blob for the multibyte EACC (CJK) set. Basic Latin (ASCII) and Extended Latin
// (ANSEL) are hand-maintained in marc8.go and deliberately skipped here.
package main

import (
	"encoding/base64"
	"encoding/xml"
	"fmt"
	"net/http"
	"os"
	"sort"
	"strconv"
	"strings"
)

const sourceURL = "https://www.loc.gov/marc/specifications/codetables.xml"

type codePoint struct {
	Marc string `xml:"marc"`
	UCS  string `xml:"ucs"`
}

type codeTables struct {
	Tables []struct {
		Sets []struct {
			Name string `xml:"name,attr"`
			ISO  string `xml:"ISOcode,attr"`
			// Single-byte sets list <code> directly; the multibyte EACC set nests
			// them inside one or more <grouping> elements.
			Code     []codePoint `xml:"code"`
			Grouping []struct {
				Code []codePoint `xml:"code"`
			} `xml:"grouping"`
		} `xml:"characterSet"`
	} `xml:"codeTable"`
}

// setVar maps a LoC character-set name to the generated decode-map identifier.
// ASCII and ANSEL are intentionally absent (kept hand-written); EACC is handled
// separately as a packed blob.
var setVar = map[string]string{
	"Basic Hebrew":      "basicHebrewDec",
	"Basic Cyrillic":    "basicCyrillicDec",
	"Extended Cyrillic": "extendedCyrillicDec",
	"Basic Arabic":      "basicArabicDec",
	"Extended Arabic":   "extendedArabicDec",
	"Basic Greek":       "basicGreekDec",
	"Greek Symbols":     "greekSymbolsDec",
	"Subscripts":        "subscriptsDec",
	"Superscripts":      "superscriptsDec",
}

func main() {
	data, err := source()
	if err != nil {
		fail(err)
	}
	var ct codeTables
	if err := xml.Unmarshal(data, &ct); err != nil {
		fail(err)
	}

	var out strings.Builder
	out.WriteString("// Code generated by internal/marc8/gen; DO NOT EDIT.\n")
	out.WriteString("// Source: " + sourceURL + " (LoC MARC-8 code tables, public domain).\n\n")
	out.WriteString("package marc8\n\n")

	var eacc [][2]uint32 // {marcCode, rune}
	// Emit single-byte maps in a stable order; collect EACC separately.
	type single struct {
		varName string
		entries [][2]uint32
	}
	singles := map[string]*single{}

	for _, t := range ct.Tables {
		for _, cs := range t.Sets {
			isEACC := strings.Contains(cs.Name, "EACC")
			vn := setVar[cs.Name]
			if vn == "" && !isEACC {
				continue // ASCII / ANSEL / anything not targeted
			}
			points := cs.Code
			for _, g := range cs.Grouping {
				points = append(points, g.Code...)
			}
			for _, c := range points {
				if c.UCS == "" {
					continue
				}
				mc, err1 := strconv.ParseUint(strings.TrimSpace(c.Marc), 16, 32)
				uc, err2 := strconv.ParseUint(strings.TrimSpace(c.UCS), 16, 32)
				if err1 != nil || err2 != nil || uc == 0 {
					continue
				}
				if isEACC {
					eacc = append(eacc, [2]uint32{uint32(mc), uint32(uc)})
					continue
				}
				s := singles[vn]
				if s == nil {
					s = &single{varName: vn}
					singles[vn] = s
				}
				s.entries = append(s.entries, [2]uint32{uint32(mc), uint32(uc)})
			}
		}
	}

	// Emit single-byte decode maps, keyed by the MARC byte in the set's home range.
	for _, vn := range []string{
		"basicHebrewDec", "basicCyrillicDec", "extendedCyrillicDec",
		"basicArabicDec", "extendedArabicDec", "basicGreekDec",
		"greekSymbolsDec", "subscriptsDec", "superscriptsDec",
	} {
		s := singles[vn]
		if s == nil {
			fail(fmt.Errorf("no entries for %s (set name changed upstream?)", vn))
		}
		sort.Slice(s.entries, func(i, j int) bool { return s.entries[i][0] < s.entries[j][0] })
		fmt.Fprintf(&out, "// %s has %d entries.\nvar %s = map[byte]rune{\n", vn, len(s.entries), vn)
		for _, e := range s.entries {
			fmt.Fprintf(&out, "\t0x%02X: 0x%04X,\n", e[0], e[1])
		}
		out.WriteString("}\n\n")
	}

	// Pack EACC as 6-byte records (3-byte MARC code + 3-byte rune), sorted by
	// MARC code, base64-encoded to keep the source compact.
	sort.Slice(eacc, func(i, j int) bool { return eacc[i][0] < eacc[j][0] })
	blob := make([]byte, 0, len(eacc)*6)
	for _, e := range eacc {
		blob = append(blob,
			byte(e[0]>>16), byte(e[0]>>8), byte(e[0]),
			byte(e[1]>>16), byte(e[1]>>8), byte(e[1]))
	}
	b64 := base64.StdEncoding.EncodeToString(blob)
	fmt.Fprintf(&out, "// eaccBlobB64 packs %d EACC entries as base64 of 6-byte records\n", len(eacc))
	out.WriteString("// (3-byte MARC code + 3-byte Unicode scalar), sorted by MARC code.\n")
	out.WriteString("const eaccBlobB64 = " + chunkString(b64, 100) + "\n")

	if err := os.WriteFile("tables_gen.go", []byte(out.String()), 0o644); err != nil {
		fail(err)
	}
	fmt.Printf("wrote tables_gen.go: %d single-byte sets, %d EACC entries\n", len(singles), len(eacc))
}

// chunkString renders s as Go source: one or more quoted string literals joined
// with +, wrapped at width characters so no single line is excessively long.
func chunkString(s string, width int) string {
	if len(s) <= width {
		return strconv.Quote(s)
	}
	var b strings.Builder
	for i := 0; i < len(s); i += width {
		end := min(i+width, len(s))
		if i > 0 {
			b.WriteString(" +\n\t")
		}
		b.WriteString(strconv.Quote(s[i:end]))
	}
	return b.String()
}

func source() ([]byte, error) {
	if len(os.Args) > 1 {
		return os.ReadFile(os.Args[1])
	}
	resp, err := http.Get(sourceURL)
	if err != nil {
		return nil, err
	}
	defer resp.Body.Close()
	if resp.StatusCode != http.StatusOK {
		return nil, fmt.Errorf("GET %s: %s", sourceURL, resp.Status)
	}
	buf := make([]byte, 0, 1<<21)
	tmp := make([]byte, 32*1024)
	for {
		n, err := resp.Body.Read(tmp)
		buf = append(buf, tmp[:n]...)
		if err != nil {
			break
		}
	}
	return buf, nil
}

func fail(err error) {
	fmt.Fprintln(os.Stderr, "gen:", err)
	os.Exit(1)
}
